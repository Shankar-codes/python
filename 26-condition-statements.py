# conditional statement - if
time = 20
if time == 20:
    print("Greeting! Shankar, It's dinner time")

# else condition
time = 20
if time == 18:
    print("Greeting! Shankar, It's dinner time")
else:
    print("It's not dinner time yet")
    

# elif statement
time_1 = 15
if time_1 == 18:
    print("It's breakfast time")
elif time_1 == 13:
    print("It's lunch time")
elif time_1 == 18:
    print("It's dinner time")
else:
    print("It's not a meal time")